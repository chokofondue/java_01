
public class DataTypeApp {

	public static void main(String[] args) {
		System.out.println(1);									//Integer	
		System.out.println("Hello World");						//String
		System.out.println("1");			
		System.out.println(1+1);								//산술연산자
		System.out.println("1"+"1");							//결합연산자
		System.out.println(1.8);								//double"
		System.out.println(Math.floor(1.8));					//Math.floor(1.8);	내림
		System.out.println(Math.ceil(1.8));						//Math.ceil(1.8);	올림
		System.out.println("Hello World".length());				//문자 갯수
		System.out.println("Hello World".contains("Hello"));	//문자가 존재하는지 유무
			
	}

}
